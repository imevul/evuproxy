package gen

import (
	"fmt"
	"os"
	"strings"

	"github.com/imevul/evuproxy/internal/config"
)

func WireGuardConf(c *config.Config) (string, error) {
	key, err := os.ReadFile(c.WireGuard.PrivateKeyFile)
	if err != nil {
		return "", fmt.Errorf("read private key: %w", err)
	}
	priv := strings.TrimSpace(string(key))

	var b strings.Builder
	fmt.Fprintf(&b, "# Generated by evuproxy — do not edit.\n\n[Interface]\n")
	fmt.Fprintf(&b, "Address = %s\n", c.WireGuard.Address)
	fmt.Fprintf(&b, "ListenPort = %d\n", c.WireGuard.ListenPort)
	fmt.Fprintf(&b, "PrivateKey = %s\n", priv)

	for _, p := range c.Peers {
		if p.Disabled {
			continue
		}
		fmt.Fprintf(&b, "\n[Peer]\n# %s\n", p.Name)
		fmt.Fprintf(&b, "PublicKey = %s\n", p.PublicKey)
		fmt.Fprintf(&b, "AllowedIPs = %s\n", peerAllowedIPs(p.TunnelIP))
		fmt.Fprintf(&b, "PersistentKeepalive = 25\n")
	}
	return b.String(), nil
}

func peerAllowedIPs(tunnelIP string) string {
	tunnelIP = strings.TrimSpace(tunnelIP)
	if strings.Contains(tunnelIP, "/") {
		return tunnelIP
	}
	return tunnelIP + "/32"
}
