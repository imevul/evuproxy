package gen

import (
	"fmt"
	"strings"

	"github.com/imevul/evuproxy/internal/config"
)

// NFTables renders standalone nftables definitions for EvuProxy tables.
func NFTables(c *config.Config) (string, error) {
	pub := c.Network.PublicInterface
	wg := c.WireGuard.Interface
	target := c.Forwarding.TargetIP
	geoSet := c.Geo.SetName
	if geoSet == "" {
		geoSet = "geo_v4"
	}

	tcpPorts, err := formatPortSets(c.Forwarding.TCPPorts)
	if err != nil {
		return "", fmt.Errorf("tcp_ports: %w", err)
	}
	udpPorts, err := formatPortSets(c.Forwarding.UDPPorts)
	if err != nil {
		return "", fmt.Errorf("udp_ports: %w", err)
	}

	var b strings.Builder
	fmt.Fprintf(&b, "# Generated by evuproxy — do not edit. Regenerate with: evuproxy reload\n\ntable inet evuproxy {\n")
	if c.Geo.Enabled {
		fmt.Fprintf(&b, `    set %s {
        type ipv4_addr
        flags interval
        auto-merge
    }

`, geoSet)
	}

	fmt.Fprintf(&b, `    chain input {
        type filter hook input priority 0; policy drop;

        ct state established,related accept
        iif "lo" accept

`)

	for _, a := range c.InputAllows {
		line, err := allowRuleLine(a)
		if err != nil {
			return "", err
		}
		fmt.Fprintf(&b, "        %s\n", line)
	}

	fmt.Fprintf(&b, "        udp dport %d accept\n", c.WireGuard.ListenPort)

	if len(tcpPorts) > 0 {
		if c.Geo.Enabled {
			fmt.Fprintf(&b, "        ip saddr @%s tcp dport %s accept\n", geoSet, tcpPorts)
			fmt.Fprintf(&b, "        tcp dport %s ip saddr != @%s limit rate 5/minute burst 20 packets log prefix \"evuproxy-geo-block: \" drop\n", tcpPorts, geoSet)
		} else {
			fmt.Fprintf(&b, "        tcp dport %s accept\n", tcpPorts)
		}
	}
	if len(udpPorts) > 0 {
		if c.Geo.Enabled {
			fmt.Fprintf(&b, "        ip saddr @%s udp dport %s accept\n", geoSet, udpPorts)
			fmt.Fprintf(&b, "        udp dport %s ip saddr != @%s limit rate 5/minute burst 20 packets log prefix \"evuproxy-geo-block: \" drop\n", udpPorts, geoSet)
		} else {
			fmt.Fprintf(&b, "        udp dport %s accept\n", udpPorts)
		}
	}

	fmt.Fprintf(&b, `    }

    chain forward {
        type filter hook forward priority 0; policy drop;

        ct state established,related accept
`)

	if len(tcpPorts) > 0 {
		fmt.Fprintf(&b, "        iifname %q oifname %q ip daddr %s tcp dport %s accept\n", pub, wg, target, tcpPorts)
	}
	if len(udpPorts) > 0 {
		fmt.Fprintf(&b, "        iifname %q oifname %q ip daddr %s udp dport %s accept\n", pub, wg, target, udpPorts)
	}

	fmt.Fprintf(&b, "        iifname %q oifname %q ct state established,related accept\n", wg, pub)
	fmt.Fprintf(&b, "        limit rate 3/minute burst 5 packets log prefix \"evuproxy-forward-drop: \" drop\n")
	fmt.Fprintf(&b, `    }
}

table ip evuproxy {
`)
	if c.Geo.Enabled {
		fmt.Fprintf(&b, `    set %s {
        type ipv4_addr
        flags interval
        auto-merge
    }

`, geoSet)
	}

	fmt.Fprintf(&b, `    chain prerouting {
        type nat hook prerouting priority -100;
`)

	if len(tcpPorts) > 0 {
		if c.Geo.Enabled {
			fmt.Fprintf(&b, "        ip saddr @%s tcp dport %s dnat to %s\n", geoSet, tcpPorts, target)
		} else {
			fmt.Fprintf(&b, "        tcp dport %s dnat to %s\n", tcpPorts, target)
		}
	}
	if len(udpPorts) > 0 {
		if c.Geo.Enabled {
			fmt.Fprintf(&b, "        ip saddr @%s udp dport %s dnat to %s\n", geoSet, udpPorts, target)
		} else {
			fmt.Fprintf(&b, "        udp dport %s dnat to %s\n", udpPorts, target)
		}
	}

	fmt.Fprintf(&b, `    }

    chain postrouting {
        type nat hook postrouting priority 100;
        oifname %q ip daddr %s masquerade
    }
}
`, wg, target)

	return b.String(), nil
}

func allowRuleLine(a config.AllowRule) (string, error) {
	p := strings.ToLower(strings.TrimSpace(a.Proto))
	if p != "tcp" && p != "udp" {
		return "", fmt.Errorf("input_allow proto must be tcp or udp")
	}
	d := strings.TrimSpace(a.DPort)
	if d == "" {
		return "", fmt.Errorf("input_allow dport is required")
	}
	if strings.ContainsAny(d, "{}") {
		return fmt.Sprintf("%s dport %s accept", p, d), nil
	}
	return fmt.Sprintf("%s dport %s accept", p, d), nil
}

// formatPortSets turns YAML port entries into an nft list: { a-b, c }.
func formatPortSets(ports []string) (string, error) {
	if len(ports) == 0 {
		return "", nil
	}
	var parts []string
	for _, raw := range ports {
		raw = strings.TrimSpace(raw)
		if raw == "" {
			continue
		}
		if strings.Contains(raw, "-") && !strings.Contains(raw, "{") {
			parts = append(parts, strings.ReplaceAll(raw, "-", "-"))
			continue
		}
		parts = append(parts, raw)
	}
	if len(parts) == 0 {
		return "", nil
	}
	return "{ " + strings.Join(parts, ", ") + " }", nil
}
