package gen

import (
	"fmt"
	"io"
	"net/http"
	"os"
	"path/filepath"
	"strings"
	"time"

	"github.com/imevul/evuproxy/internal/config"
)

const ipDenyCountryURL = "https://www.ipdeny.com/ipblocks/data/countries/%s.zone"

// GeoLoaderNFT writes a file that flushes and repopulates geo sets in both inet and ip tables.
func GeoLoaderNFT(c *config.Config, zoneDir string) (string, error) {
	if !c.Geo.Enabled {
		return "", fmt.Errorf("geo is disabled in config")
	}
	set := c.Geo.SetName
	if set == "" {
		set = "geo_v4"
	}
	var elems []string
	for _, cc := range c.Geo.Countries {
		cc = strings.ToLower(strings.TrimSpace(cc))
		path := filepath.Join(zoneDir, cc+".zone")
		b, err := os.ReadFile(path)
		if err != nil {
			return "", fmt.Errorf("read zone %s: %w", path, err)
		}
		for _, line := range strings.Split(string(b), "\n") {
			line = strings.TrimSpace(line)
			if line == "" || strings.HasPrefix(line, "#") {
				continue
			}
			elems = append(elems, line)
		}
	}
	if len(elems) == 0 {
		return "", fmt.Errorf("no IP ranges loaded for geo (empty zone files?)")
	}
	payload := strings.Join(elems, ", ")

	var b strings.Builder
	fmt.Fprintf(&b, "# Generated by evuproxy update-geo\n")
	fmt.Fprintf(&b, "flush set inet evuproxy %s\n", set)
	fmt.Fprintf(&b, "add element inet evuproxy %s { %s }\n", set, payload)
	fmt.Fprintf(&b, "flush set ip evuproxy %s\n", set)
	fmt.Fprintf(&b, "add element ip evuproxy %s { %s }\n", set, payload)
	return b.String(), nil
}

// DownloadZones fetches country zone files into zoneDir.
func DownloadZones(c *config.Config) error {
	if err := os.MkdirAll(c.Geo.ZoneDir, 0o755); err != nil {
		return err
	}
	client := &http.Client{Timeout: 2 * time.Minute}
	for _, cc := range c.Geo.Countries {
		cc = strings.ToLower(strings.TrimSpace(cc))
		url := fmt.Sprintf(ipDenyCountryURL, cc)
		req, err := http.NewRequest(http.MethodGet, url, nil)
		if err != nil {
			return err
		}
		resp, err := client.Do(req)
		if err != nil {
			return fmt.Errorf("download %s: %w", url, err)
		}
		body, err := io.ReadAll(resp.Body)
		resp.Body.Close()
		if err != nil {
			return err
		}
		if resp.StatusCode != http.StatusOK {
			return fmt.Errorf("download %s: HTTP %s", url, resp.Status)
		}
		out := filepath.Join(c.Geo.ZoneDir, cc+".zone")
		if err := os.WriteFile(out, body, 0o644); err != nil {
			return err
		}
	}
	return nil
}
